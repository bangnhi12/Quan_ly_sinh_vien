    CONSTRAINT fk_nganh_khoa FOREIGN KEY (MaKhoa) REFERENCES Khoa(MaKhoa)
);

-- 4. LOP
CREATE TABLE Lop (
    MaLop VARCHAR(11) PRIMARY KEY,
